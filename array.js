// ARRAY -> ARRAY is Just Indexed  Collection of Element
const array = [1, 2.3 , "nazeer"]
console.log(array) 

// ARRAY METHOD 
// Push Method -> add in last index
array.push("khan")
console.log(array)
// pop  Method -> remove in last index
array.pop()
console.log(array)
// unshift -> add in first index of array
array.unshift("khan bhai")
console.log(array)
// shift -> remove in first index of array
array.shift()
console.log(array)